package Day02;

public class Ex12_PlusMinus {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c;
		
		//++a, a++			: a를 1증가
		
		
		
		c = ++a + b;
		System.out.println("c = " + c);
		System.out.println("a = " + a);
		
		
		int x = 10;
		int y = 20;
		int z;
		
		
		z = x++ + y;
		System.out.println("z = " + z);
		System.out.println("x = " + x);
		
	}

}
